     */
    public function update(Request $request, User $user)
    {
        // dd($request->all(), $user);
        $role = $request->role;
        $roles = Role::whereIn('name', [$role])->pluck('name')->toArray();
        // $user->assignRole($roles);
        $user->syncRoles($roles);

        return redirect()->route('user.show', $user->id);
    }

